    private void IniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IniciarActionPerformed
        // TODO add your handling code here:
        if (Jugador1.getText().equals("")
                || Jugador2.getText().equals("")
                || Jugador1.getText().equals(Jugador2.getText())) {
            JOptionPane.showMessageDialog(null, "¡Los nombres de los jugadores son requeridos y diferentes!");
        } else {
            if (Color1.getSelectedIndex() == Color2.getSelectedIndex()) {
                JOptionPane.showMessageDialog(null, "¡Los colores de los jugadores son requeridos y diferentes!");
            } else {
                if (Ficha1.getText().equals("")
                        || Ficha2.getText().equals("")
                        || Ficha1.getText().equals(Ficha2.getText())) {
                    JOptionPane.showMessageDialog(null, "¡Las ficha de los jugadores son requeridas y diferentes!");
                } else {
                    if (Tablero.getSelectedIndex() == 0) {
                        JOptionPane.showMessageDialog(null, "¡El tipo de tablero es requerido!");
                    } else {
                        Jugador jugadorUno = new Jugador(
                                Jugador1.getText(),
                                Color1.getItemAt(Color1.getSelectedIndex()),
                                Ficha1.getText()
                        );
                        Jugador jugadorDos = new Jugador(
                                Jugador2.getText(),
                                Color2.getItemAt(Color2.getSelectedIndex()),
                                Ficha2.getText()
                        );

                        String tipoDeTablero = Tablero.getItemAt(Tablero.getSelectedIndex());
                        String tamañoTablero[] = tipoDeTablero.split(" x ");

                        int dimensionTablero[][] = new int[Integer.parseInt(tamañoTablero[0])][Integer.parseInt(tamañoTablero[1])];

                        Tablero tablero = new Tablero(dimensionTablero);
                        Jugador jugadores[] = {jugadorUno, jugadorDos};

                        juego.iniciarPartida(jugadores, tablero);

                        enabledInicioDePartida();

                        JOptionPane.showMessageDialog(null, "¡Partida iniciada!");
                    }
                }
            }
        }
    }//GEN-LAST:event_IniciarActionPerformed

