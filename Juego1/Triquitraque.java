    public void moverFicha(JLabel label, int fila, int columna) {
        String salida = juego.manejarEntradaUsuario(fila, columna, auxiliarJugador);

        if (salida.contains("mover")) {
            JOptionPane.showMessageDialog(null, salida);
            return;
        }

        cantidadDeMovimientos--;
        label.setForeground(getColor(juego.getJugadores()[auxiliarJugador - 1].getColor()));
        label.setText(String.valueOf(juego.getJugadores()[auxiliarJugador - 1].getFicha()));

        if (salida.contains("Gana")) {
            JOptionPane.showMessageDialog(null, salida);
            ultimoMovimeinto();
            return;
        }
        if (salida.contains("empate") && cantidadDeMovimientos == 0) {
            JOptionPane.showMessageDialog(null, salida);
            ultimoMovimeinto();
            return;
        }
        if (salida.contains("turno")) {
            if (auxiliarJugador == 1) {
                auxiliarJugador = 2;
            } else {
                auxiliarJugador = 1;
            }
            JOptionPane.showMessageDialog(null, salida);
        }
    }

